public class Kendaraan{
    String noTNKBS, nama, jenis;
    int cc, tahun, bulanHarusBayar;

    public Kendaraan(String noTNKBS, String nama, String jenis, int cc, int tahun, int bulanHarusBayar )    {
        this.noTNKBS = noTNKBS;
        this.nama = nama;
        this.jenis = jenis;
        this.cc = cc;
        this.tahun = tahun;
        this.bulanHarusBayar = bulanHarusBayar;

    }
}